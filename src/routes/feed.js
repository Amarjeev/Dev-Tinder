// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
const feedRoute = express.Router(); // Initialize a new Express Router

// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
// ğŸ“ Route: GET /alluser/:userId
// ğŸ“„ Description:
//   Retrieve all users excluding:
//   1ï¸âƒ£ The current user
//   2ï¸âƒ£ Users who are already in 'accepted' or 'interested' connection states
// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
feedRoute.get("/alluser/:userId", async (req, res) => {
  try {
    // ğŸ§  Step 1: Extract userId from URL params and convert to ObjectId
    const userId = req.params.userId;
    const objectId = new ObjectId(userId);

    // ğŸ” Step 2: Find all connection requests where the user is involved
    const response = await RequestModel.find({
      $or: [
        { fromUserId: objectId },
        { toUserId: objectId }
      ],
      status: { $in: ['accepted', 'interested'] }
    }).select("fromUserId toUserId fromUserName toUserName status");

    // ğŸ§¾ Step 3: Flatten all user IDs involved in those requests
    const userIdsInRequests = response.flatMap(doc => [
      doc.fromUserId.toString(),
      doc.toUserId.toString()
    ]);

    // ğŸ“¦ Step 4: Fetch all users excluding the current user and users in requests
    const alluserData = await User.find({
      _id: { $nin: [objectId, ...userIdsInRequests] }
    });

    // âœ… Step 5: Send successful response with filtered users
    res.status(200).send({ alluserData });

  } catch (error) {
    // âŒ Error Handling
    console.error("âŒ Error fetching users:", error);
    res.status(500).send({ error: error.message });
  }
});

