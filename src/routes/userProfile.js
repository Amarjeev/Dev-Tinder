const express = require("express");
const { userAuth } = require("../middlewares/auth"); // Middleware to verify user authentication
const userProfileRouter = express.Router();

// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
// ğŸ“„ Route: GET /profile
// Access: ğŸ”’ Protected (requires authentication)
// Purpose: Retrieve the authenticated user's profile data
// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
userProfileRouter.get("/profile", userAuth, async (req, res) => {
  try {
    // âœ… Get user data from the auth middleware
    const UserData = req.userData;

    // ğŸ“¤ Send user data back as response
    res.status(200).send(UserData);
  } catch (error) {
    // âŒ Handle and report any server-side errors
    console.error("Error fetching profile:", error);
    res.status(500).json({ error: error.message });
  }
});

