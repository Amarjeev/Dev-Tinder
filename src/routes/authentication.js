const express = require("express");
const bcrypt = require("bcrypt");
const User = require("../models/user");
const { validateSignupData } = require("../Helpers/Validation");

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// ğŸš€ Initialize Router
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const authRouter = express.Router();

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// âœï¸ POST /signup â€” Register a new user
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
authRouter.post("/signup", async (req, res) => {
  const { name, email, age, gender, password, photoUrl } = req.body;

  try {
    // ğŸ” Check if email already exists in DB
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({
        message: "ğŸ“§ Email is already registered. Please use another email address.",
      });
    }

    // âœ”ï¸ Validate signup data format and requirements
    validateSignupData(req);

    // ğŸ”’ Hash the password securely
    const passwordHash = await bcrypt.hash(password, 10);

    // ğŸ†• Create new user document
    const user = new User({
      name,
      email,
      age,
      gender,
      photoUrl,
      password: passwordHash,
    });

    // ğŸ’¾ Save user to database
    await user.save();

    console.log("âœ… User signup successful");
    res.status(201).send("User added successfully.");
  } catch (error) {
    // âš ï¸ Handle errors during signup
    console.error("âŒ Signup error:", error);
    res.status(500).send(error.message);
  }
});

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// ğŸ” POST /login â€” Authenticate user & issue JWT token
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
authRouter.post("/login", async (req, res) => {
  const { email, password } = req.body;

  try {
    // ğŸ” Find user by email
    const userData = await User.findOne({ email });

    if (!userData) {
      console.error("âŒ Login failed: Email not found");
      return res.status(400).json({ emailerror: "Invalid email or password" });
    }

    // ğŸ”‘ Validate password
    const isPasswordMatch = await userData.validatePassword(password);

    if (!isPasswordMatch) {
      console.error("âŒ Login failed: Incorrect password");
      return res.status(400).json({ emailerror: "Invalid email or password" });
    }

    // ğŸ« Generate JWT token for authentication
    const token = await userData.getJWT();

    // ğŸª Set token in HTTP-only cookie
    res.cookie("token", token, { httpOnly: true, secure: false });

    // ğŸ“¤ Prepare user data to send back
    const userObj = userData.toObject();

    // âœ… Respond with user info and token
    res.status(200).json({
      userObj,
      token,
    });

    console.log("âœ… Login successful");
  } catch (error) {
    // âš ï¸ Handle errors during login
    console.error("âŒ Login error:", error);
    res.status(500).send(error.message);
  }
});

